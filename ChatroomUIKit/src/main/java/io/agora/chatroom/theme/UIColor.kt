enum class LightnessThemeStyle(val value: Float){
    ZERO(0f),
    TEN(10f),
    TWENTY(20f),
    THIRTY(30f),
    FORTY(40f),
    FIFTY(50f),
    SIXTY(60f),
    SEVENTY(70f),
    SEVENTY_FIVE(75f),
    EIGHTY(80f),
    EIGHTY_FIVE(85f),
    NINETY(90f),
    NINETY_FIVE(95f),
    NINETY_EIGHT(98f),
    ONE_HUNDRED(100f)
}

enum class AlphaThemeStyle(val value: Float){
    ZERO(0f),
    TEN(0.1f),
    TWENTY(0.2f),
    THIRTY(0.3f),
    FORTY(0.4f),
    FIFTY(0.5f),
    SIXTY(0.6f),
    SEVENTY(0.7f),
    EIGHTY(0.8f),
    NINETY(0.9f),
    NINETY_FIVE(0.95f),
    NINETY_EIGHT(0.98f),
    ONE_HUNDRED(1f)
}

private fun primaryColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(primaryHue,1f,lightnessStyle.value/100,1f)
}

private fun primaryEndColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(primaryEndHue,1f,lightnessStyle.value/100,1f)
}

private fun secondaryColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(secondaryHue,1f,lightnessStyle.value/100,1f)
}

private fun errorColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(errorHue,1f,lightnessStyle.value/100,1f)
}

private fun neutralColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(neutralHue,0.08f,lightnessStyle.value/100,1f)
}

private fun neutralSpecialColor(lightnessStyle:LightnessThemeStyle):Color{
    return Color.hsl(neutralSpecialHue,0.36f,lightnessStyle.value/100,1f)
}

private fun barrageColor(lightnessStyle:LightnessThemeStyle,alphaStyle:AlphaThemeStyle):Color{
    return Color.hsl(0f,0f,lightnessStyle.value/100,alphaStyle.value)
}

